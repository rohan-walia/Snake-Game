package SnakeGamePractice;

public class Food {
    int row;
    int col;
    public Food() {
        row = 0;
        col = 0;
    }
    public void setRow(int row) {
        this.row = row;
    }
    public void setCol(int col) {
        this.col = col;
    }
    public int getRow() {
        return row;
    }
    public int getCol() {
        return col;
    }
}
